    private void chCappuccinoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_chCappuccinoMouseClicked
        // TODO add your handling code here:
        double cCap = Double.parseDouble(lblCostOfDrinks.getText()); //actual total cost of drink
        double Cap = Double.parseDouble(txtCappuccino.getText()); //qty
        double iCap = 300; //cost of cappuccino
        
        if(chCappuccino.isSelected()){
            i[3] = (Cap * iCap) + cCap; // i -> Increment
            String pDrink = String.format("%.2f", i[3]); //new compiled cost of drinks
            lblCostOfDrinks.setText(pDrink);
            String cost = String.format("%.2f", (Cap * iCap)); //cost of cafe
            txtCappuccino.setText(cost);           
        }
    }//GEN-LAST:event_chCappuccinoMouseClicked

