    /**
     * compute total button
    */
    private void btnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalActionPerformed
        // TODO add your handling code here:
        total[0] = Double.parseDouble(lblCostOfDrinks.getText());
        total[1] = Double.parseDouble(lblCostOfCakes.getText());
        total[2] = Double.parseDouble(lblServiceCharge.getText());
        
        double cTotal = (total[0] + total[1] + total[2]); //total of all costs
        
        String iTaxTotal = String.format("%.2f", cTotal / 100); //tax calculation (1%)
        lblTaxCost.setText(iTaxTotal);
        
        double subTotal = (cTotal);
        String iSubTotal = String.format("%.2f", subTotal); //total before tax
        lblSubTotal.setText(iSubTotal);
        
        double allTotal = (subTotal + (cTotal/100));
        String iAllTotal = String.format("%.2f", allTotal); //total before tax
        lblTotalCost.setText(iAllTotal);
        
    }//GEN-LAST:event_btnTotalActionPerformed

