    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        boolean credentials = false;
        if(txtUsername.getText().equals("") || txtPassword.getText().equals("")){ //check fields empty
            JOptionPane.showMessageDialog(null, "Please Fill in Username or password", "Fields Empty", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                UserAuth userAuth = new UserAuth();
                credentials = userAuth.getLogin(txtUsername.getText(), txtPassword.getText());

                if(credentials==true) {
                    Cafe_POS cafe = new Cafe_POS();
                    //JOptionPane.showMessageDialog(null, "Welcome User"); //Welcome message
                    //this.setVisible(false);
                    this.dispose(); //close current window (login)
                    cafe.setVisible(true); //open cafe working window
                } else {
                    JOptionPane.showMessageDialog(null, "Login Failed! \nPlease check your credentials", "Access Denied", JOptionPane.ERROR_MESSAGE);
                }

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
                //ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

